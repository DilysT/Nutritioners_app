class MealItem {
  final int foodId;
  final String name;
  final int kcal;
  final int portion;
  final double fat;
  final double protein;
  final double carbs;
  final double fiber;
  final double cholesterol;

  MealItem({
    required this.foodId,
    required this.name,
    required this.kcal,
    required this.portion,
    required this.fat,
    required this.protein,
    required this.carbs,
    required this.fiber,
    required this.cholesterol,
  });
}
